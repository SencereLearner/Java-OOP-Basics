package Challenges.CompositionChallenge;

public class Utencils {
  int knifes, spoons, forks;

  public Utencils(int knifes, int spoons, int forks) {
    this.knifes = knifes;
    this.spoons = spoons;
    this.forks = forks;
  }

  public int getKnifes() {
    return knifes;
  }

  public int getSpoons() {
    return spoons;
  }

  public int getForks() {
    return forks;
  }
}
